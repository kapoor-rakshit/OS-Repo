# nohup
# When using the command shell, prefixing a command with nohup prevents the command from being aborted automatically when you log out or exit the shell.
# The name nohup stands for "no hangup." 
# The hangup (HUP) signal, which is normally sent to a process to inform it that the user has logged off (or "hung up"), 
# is intercepted by nohup, allowing the process to continue running.

nohup COMMAND

